public class abc {
	String manufacturer;
	int year;
	int size;

	public abc(String manufacturer, int year, int size)
	{
		this.manufacturer = manufacturer;
		this.year = year;
		this.size = size;
	}

	void show()
	{
		System.out.print(manufacturer + "에서 만든");
		System.out.print(year + " 년형");
		System.out.print(size + " 인치");
	}
	public static void main(String[] args){
		abc myTV = new abc("LG", 2017, 32);
		myTV.show();
	}
}
